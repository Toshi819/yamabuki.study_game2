let gameMode = "normal";
let timer;
let timeLeft = 10;   // 1問あたり10秒
let combo = 0;

function startQuiz(mode) {
  gameMode = mode;
  currentIndex = 0;
  score = 0;
  combo = 0;
  showQuestion();
}

function showQuestion() {
  const question = currentQuiz[currentIndex];

  let html = `
    <h2>問題 ${currentIndex + 1} / ${currentQuiz.length}</h2>
  `;

  if (gameMode === "time") {
    timeLeft = 10;
    html += `<p>残り時間: <span id="timer">${timeLeft}</span> 秒</p>`;
  }

  html += `<p>${question.q}</p>`;

  question.c.forEach((choice, index) => {
    html += `<button id="choice${index}" class="quiz-btn">${choice}</button>`;
  });

  html += `<button id="backBtn" class="back-btn">中断して戻る</button>`;

  app.innerHTML = html;
  html += `<div id="comboEffect" class="combo-effect"></div>`;
  html += `<div id="legendEffect" class="legend-effect"></div>`;


  question.c.forEach((_, index) => {
    document.getElementById(`choice${index}`)
      .addEventListener("click", (e) => checkAnswer(e.target, index));
  });

  document.getElementById("backBtn")
    .addEventListener("click", startGame);

  if (gameMode === "time") {
    startTimer();
  }
}


function startTimer() {
  clearInterval(timer);

  const timerElement = document.getElementById("timer");

  timer = setInterval(() => {
    timeLeft--;
    timerElement.textContent = timeLeft;

    if (timeLeft <= 0) {
      clearInterval(timer);
      combo = 0;
      score -= 5;
      nextQuestion();
    }
  }, 1000);
}

function showComboEffect() {
  const effect = document.getElementById("comboEffect");
  if (!effect) return;

  let color = "#00e5ff";

  if (combo >= 3) color = "#00ff88";
  if (combo >= 5) color = "#ffd700";
  if (combo >= 8) color = "#ff3b3b";

  effect.style.color = color;
  effect.textContent = `🔥 ${combo} COMBO!!`;

  effect.classList.remove("combo-show");
  void effect.offsetWidth; // 再アニメーション用リセット
  effect.classList.add("combo-show");
}

function checkAnswer(button, selected) {
  const question = currentQuiz[currentIndex];
  if (gameMode === "time") {
    clearInterval(timer);
  }
  const buttons = document.querySelectorAll(".quiz-btn");

  // 全ボタンを無効化
  buttons.forEach(btn => btn.disabled = true);

  if (selected === question.a) {
    if (gameMode === "time") {
      combo++;
      score += 10 + combo; // コンボボーナス
      showComboEffect();
      

    } else {
      score += 10;
    }
    button.classList.add("correct");
  } else {
    combo = 0;
    button.classList.add("wrong");

    // 正解ボタンも光らせる
    buttons.forEach(btn => {
      if (parseInt(btn.dataset.index) === question.a) {
        btn.classList.add("correct");
      }
    });
  }

  setTimeout(() => {
    showExplanation(question);
  }, 1000);
}
function showExplanation(question) {
  app.innerHTML = `
    <h2>${score} 点</h2>
    <p>解説: ${question.e}</p>
    <button id="nextQuestionBtn" class="main-btn">次へ</button>
  `;
  document.getElementById("nextQuestionBtn").addEventListener("click", nextQuestion);
}

function nextQuestion() {
  currentIndex++;

  if (currentIndex < currentQuiz.length) {
    showQuestion();
  } else {
    finishQuiz();
  }
}

async function finishQuiz() {
  const subject = localStorage.getItem("selectedSubject");
  const round = localStorage.getItem("selectedRound");
  const key = subject + "_" + round;

  const currentId = localStorage.getItem("currentUser");

  if (gameMode === "time") {
    const perfectScore = currentQuiz.length * 10;
    if (score >= perfectScore) {
      score += 5; // パーフェクトボーナス
    }
  }

  const userRef = doc(db, "users", currentId);
  const userSnap = await getDoc(userRef);

  if (!userSnap.exists()) {
    alert("ユーザーデータが見つかりません");
    return;
  }

  const userData = userSnap.data();

  const scores = userData.scores || {};
  const previousBest = scores[key] || 0;

  // 🔥 ベスト更新判定
  if (score > previousBest) {
    scores[key] = score;
  }

  // 🔥 合計ベスト更新
   const totalScore = Object.values(scores)
    .reduce((sum, val) => sum + Number(val), 0);

  await updateDoc(userRef, {
    scores: scores,
    totalScore: totalScore
  });

  app.innerHTML = `
  <h2>クイズ終了！</h2>
  <p>スコア: ${score}</p>
  <button id="homeBtn" class="main-btn">ホームへ</button>
`;

  document.getElementById("homeBtn")
    .addEventListener("click", goHome);

}



